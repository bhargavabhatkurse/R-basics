#arithmetic operator
#+ - * / %% %/% ^

a<-7;b<-2
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b) #remainder
print(a%/%b)#quotient
print(a^b)

#vector -> collection of similar datatypes
a<-c(8,9,6)
b<-c(1,2,3)

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b) #remainder
print(a%/%b)#quotient
print(a^b)


#relational: < > == <= >= !=
a<-7;b<-2
print(a<b)
print(a>b)
print(a==b)
print(a<=b)
print(a>=b) 
print(a!=b)


#logical
d<-c(TRUE,TRUE,TRUE)
e<-c(3.1,FALSE,2+5i)
print(d&e) #element-wise logical operations
print(d&&e)
print(d|e)
print(d||e)
print(!d) #not

